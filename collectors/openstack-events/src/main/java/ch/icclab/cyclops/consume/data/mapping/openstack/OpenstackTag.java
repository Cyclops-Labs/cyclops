package ch.icclab.cyclops.consume.data.mapping.openstack;

/**
 * Author: Oleksii Serhiienko
 * Updated on: 12-Jan-01
 * Description: This class holds Tag structure.
 * influxdb query: SHOW TAG VALUES FROM "measurement" WITH KEY = "key"
 * */
public class OpenstackTag {
    public OpenstackTag(){
    }
    public String value;

    public String key;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
